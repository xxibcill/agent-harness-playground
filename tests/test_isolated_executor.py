def test_worker_with_process_isolation_completes_run() -> None:
    """Test that the worker correctly uses IsolatedExecutor when isolation is enabled."""
    store = InMemoryRunStore()
    run = store.create_run(CreateRunRequest(input="worker isolation test"))

    config = WorkerConfig(
        poll_interval_seconds=0.0,
        lease_seconds=30,
        worker_id="worker-isolation-test",
        enable_process_isolation=True,
        cpu_limit_seconds=60,
        memory_limit_mb=256,
    )

    resource_limits = ResourceLimits(
        cpu_seconds=config.cpu_limit_seconds,
        memory_mb=config.memory_limit_mb,
    )
    executor = IsolatedExecutor(store, resource_limits=resource_limits)

    did_work = run_once(store, executor, config)

    assert did_work is True
    completed = store.get_run(run.run_id)
    assert completed is not None
    assert completed.status == RunStatus.COMPLETED


