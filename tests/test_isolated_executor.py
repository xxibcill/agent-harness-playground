def test_events_are_relayed_from_subprocess() -> None:
    """Verify that events generated in the subprocess are relayed to the store."""
    store = InMemoryRunStore()
    run = store.create_run(CreateRunRequest(input="event relay test"))
    resource_limits = ResourceLimits(cpu_seconds=60, memory_mb=256)

    executor = IsolatedExecutor(store, resource_limits=resource_limits)
    executor.execute(run)

    events = store.list_events(run.run_id)

    # Check for key events that should have been relayed
    # Note: run.started is generated by the worker when claiming, not by executor
    event_types = [event.event_type for event in events]
    expected_events = [
        "run.created",
        "run.queued",
        "workflow.started",
        "node.started",
        "tool.started",
        "tool.completed",
        "node.completed",
        "model.started",
        "model.completed",
        "workflow.completed",
        "run.completed",
    ]

    for expected in expected_events:
        assert expected in event_types, f"Missing event: {expected}"


def test_subprocess_failure_handling() -> None:
    """Test that subprocess failures are handled gracefully."""
    from agent_harness_core.isolated_executor import SubprocessEvent

    # Create a queue and a run
    event_queue: mp.Queue = mp.Queue()
    store = InMemoryRunStore()
    run = store.create_run(CreateRunRequest(input="failure test"))

    # Test that we can put and get events from the queue
    test_event = SubprocessEvent(
        event_type="test.event",
        category="test",
        payload={"test": True},
    )
    event_queue.put(test_event)

    # Verify the event can be retrieved
    queued_event = event_queue.get(timeout=1.0)
    assert isinstance(queued_event, SubprocessEvent)
    assert queued_event.event_type == "test.event"

    # Clean up
    event_queue.close()
    event_queue.join_thread()


def test_multiple_runs_dont_interfere() -> None:
    """Test that multiple runs can execute without interfering with each other."""
    store = InMemoryRunStore()
    resource_limits = ResourceLimits(cpu_seconds=60, memory_mb=256)
    executor = IsolatedExecutor(store, resource_limits=resource_limits)

    run1 = store.create_run(CreateRunRequest(input="first run"))
    run2 = store.create_run(CreateRunRequest(input="second run"))

    result1 = executor.execute(run1)
    result2 = executor.execute(run2)

    assert result1.status == RunStatus.COMPLETED
    assert result1.output["response"] == "Echo: first run"

    assert result2.status == RunStatus.COMPLETED
    assert result2.output["response"] == "Echo: second run"

    # Verify events are separate
    events1 = store.list_events(run1.run_id)
    events2 = store.list_events(run2.run_id)

    assert all(e.run_id == run1.run_id for e in events1)
    assert all(e.run_id == run2.run_id for e in events2)


