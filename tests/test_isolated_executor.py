"""Tests for process-based isolation in IsolatedExecutor."""
from __future__ import annotations

import multiprocessing as mp
from typing import Any

import pytest
from agent_harness_contracts import CreateRunRequest, RunStatus
from agent_harness_core import InMemoryRunStore, IsolatedExecutor, ResourceLimits
from agent_harness_worker.main import WorkerConfig, run_once


def test_isolated_executor_completes_run_in_subprocess() -> None:
    """Verify that a run completes successfully when executed in an isolated subprocess."""
    store = InMemoryRunStore()
    run = store.create_run(CreateRunRequest(input="isolated execution test"))
    resource_limits = ResourceLimits(cpu_seconds=60, memory_mb=256)

    executor = IsolatedExecutor(store, resource_limits=resource_limits)
    result = executor.execute(run)

    assert result.status == RunStatus.COMPLETED
    assert result.output is not None
    assert result.output["response"] == "Echo: isolated execution test"

    # Verify events were persisted
    events = store.list_events(run.run_id)
    event_types = [event.event_type for event in events]
    assert "workflow.started" in event_types
    assert "workflow.completed" in event_types
    assert "run.completed" in event_types


